    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
         try {
        String idAnalisis = txtIdAnalisis.getText();

       
        CAnalisisDeSangre controladorAnalisis = new CAnalisisDeSangre();
        boolean eliminado = controladorAnalisis.eliminarAnalisis(idAnalisis);

        if (eliminado) {
            JOptionPane.showMessageDialog(this, "Análisis eliminado correctamente.");
           
            controladorAnalisis.cargarTablaAnalisis(tblAnalisisDeSangre); 
            limpiarCampos(); // Limpiar campos después de eliminar
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo eliminar el análisis.");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Por favor, ingrese un ID de análisis válido.");
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

