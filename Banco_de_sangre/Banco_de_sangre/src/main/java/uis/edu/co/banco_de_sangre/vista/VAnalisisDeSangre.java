    public void cargarDatos(){
    
   int fila = tblAnalisisDeSangre.getSelectedRow();
    
    
    txtIdAnalisis.setText(tblAnalisisDeSangre.getValueAt(fila, 0).toString());
    txtUnidad.setText(tblAnalisisDeSangre.getValueAt(fila, 1).toString());
    txtFechaAnalisis.setText(tblAnalisisDeSangre.getValueAt(fila, 2).toString());
    
    
    String resultadoAnalisis = tblAnalisisDeSangre.getValueAt(fila, 3).toString();
    
    
    for (int i = 0; i < cmbResultadoAnalisis.getItemCount(); i++) {
        if (cmbResultadoAnalisis.getItemAt(i).equals(resultadoAnalisis)) {
            cmbResultadoAnalisis.setSelectedIndex(i);
            break;
        }
    }
    }

