    private void btnConsultarPorFechaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarPorFechaActionPerformed
        // TODO add your handling code here:
        String fechaInicio = JOptionPane.showInputDialog(this, "Ingrese la fecha de inicio (YYYY-MM-DD):");
        String fechaFin = JOptionPane.showInputDialog(this, "Ingrese la fecha de fin (YYYY-MM-DD):");

        if (fechaInicio != null && fechaFin != null) {
            canalisis.consultarAnalisisPorFecha(tblAnalisisDeSangre, fechaInicio, fechaFin);
        } else {
            JOptionPane.showMessageDialog(this, "Por favor ingrese fechas válidas.");
        }
    }//GEN-LAST:event_btnConsultarPorFechaActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
    String idAnalisis = txtIdAnalisis.getText();
    String unidad = txtUnidad.getText();
    String fechaAnalisis = txtFechaAnalisis.getText();
    String resultadoAnalisis = cmbResultadoAnalisis.getSelectedItem().toString();
    
    
    if (idAnalisis.isEmpty() || unidad.isEmpty() || fechaAnalisis.isEmpty() || resultadoAnalisis.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor complete todos los campos.");
        return;
    }
    
   
    CAnalisisDeSangre controladorAnalisis = new CAnalisisDeSangre();
    controladorAnalisis.registrarAnalisis(idAnalisis, unidad, fechaAnalisis, resultadoAnalisis);
    
   
    controladorAnalisis.cargarTablaAnalisis(tblAnalisisDeSangre);
    
    
    limpiarCampos();
    }//GEN-LAST:event_btnRegistrarActionPerformed

